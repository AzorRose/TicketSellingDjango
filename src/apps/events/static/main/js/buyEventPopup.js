function openModal() {
    document.getElementById('myModal').style.display = 'block';
    document.getElementById('overlay').style.display = 'block';
}

function closeModal() {
    document.getElementById('myModal').style.display = 'none';
    document.getElementById('overlay').style.display = 'none';
}

function openDancefloorPopup() {
    document.getElementById('dancefloor-popup').style.display = 'block';
    document.getElementById('dancefloor-overlay').style.display = 'block';
}

function closeDancefloorPopup() {
    document.getElementById('dancefloor-popup').style.display = 'none';
    document.getElementById('dancefloor-overlay').style.display = 'none';
}